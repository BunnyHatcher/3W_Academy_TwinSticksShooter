		private struct VertexKey{
	        private readonly long _x;
	        private readonly long _y;
	        private readonly long _z;

	        // Change this if you require a different precision.
	        private const int Tolerance = 100000;

	        // Magic FNV values. Do not change these.
	        private const long FNV32Init = 0x811c9dc5;
	        private const long FNV32Prime = 0x01000193;

	        public VertexKey(Vector3 position) {
	            _x = (long)(Mathf.Round(position.x * Tolerance));
	            _y = (long)(Mathf.Round(position.y * Tolerance));
	            _z = (long)(Mathf.Round(position.z * Tolerance));
	        }

	        public override bool Equals(object obj) {
	            var key = (VertexKey)obj;
	            return _x == key._x && _y == key._y && _z == key._z;
	        }

	        public override int GetHashCode() {
	            long rv = FNV32Init;
	            rv ^= _x;
	            rv *= FNV32Prime;
	            rv ^= _y;
	            rv *= FNV32Prime;
	            rv ^= _z;
	            rv *= FNV32Prime;

	            return rv.GetHashCode();
	        }
	    }

	    private struct VertexEntry {
	        public int MeshIndex;
	        public int TriangleIndex;
	        public int VertexIndex;

	        public VertexEntry(int meshIndex, int triIndex, int vertIndex) {
	            MeshIndex = meshIndex;
	            TriangleIndex = triIndex;
	            VertexIndex = vertIndex;
	        }
	    }

		public static void freezeMeshPostPass(Mesh mesh, float angle, GameObject obj = null, BoneWeight1[] boneWeights = null, byte[] numBonesPerVert = null, int maxBonesPerVert = 16){
            // collect unwelded vertices in hashed list
            // average normals
            // average weights
            // store each triangle center as UV for frozen mesh splatting

            float objScale = 1.0f;
            if(obj != null){
                objScale = obj.transform.lossyScale.x;
            }

	        var cosineThreshold = Mathf.Cos(angle * Mathf.Deg2Rad);

	        var vertices = mesh.vertices;
	        var normals = new Vector3[vertices.Length];

	        // Holds the normal of each triangle in each sub mesh.
	        var triNormals = new Vector3[mesh.subMeshCount][];

            Vector2[] triangleCenterUV1 = new Vector2[vertices.Length];
            Vector2[] triangleCenterUV2 = new Vector2[vertices.Length];

            Dictionary<int, float> weightDict = new Dictionary<int, float>(maxBonesPerVert);
            
	        var dictionary = new Dictionary<VertexKey, List<VertexEntry>>(vertices.Length);

	        for (var subMeshIndex = 0; subMeshIndex < mesh.subMeshCount; ++subMeshIndex) {
	            
	            var triangles = mesh.GetTriangles(subMeshIndex);

	            triNormals[subMeshIndex] = new Vector3[triangles.Length / 3];

                Vector3 triangleCenter;

	            for (var i = 0; i < triangles.Length; i += 3) {
	                int i1 = triangles[i];
	                int i2 = triangles[i + 1];
	                int i3 = triangles[i + 2];

	                // Calculate the normal of the triangle
	                Vector3 p1 = vertices[i2] - vertices[i1];
	                Vector3 p2 = vertices[i3] - vertices[i1];
	                Vector3 normal = Vector3.Cross(p1, p2).normalized;
	                int triIndex = i / 3;
	                triNormals[subMeshIndex][triIndex] = normal;

	                List<VertexEntry> entry;
	                VertexKey key;

	                if (!dictionary.TryGetValue(key = new VertexKey(vertices[i1]), out entry)) {
	                    entry = new List<VertexEntry>(4);
	                    dictionary.Add(key, entry);
	                }
	                entry.Add(new VertexEntry(subMeshIndex, triIndex, i1));

	                if (!dictionary.TryGetValue(key = new VertexKey(vertices[i2]), out entry)) {
	                    entry = new List<VertexEntry>();
	                    dictionary.Add(key, entry);
	                }
	                entry.Add(new VertexEntry(subMeshIndex, triIndex, i2));

	                if (!dictionary.TryGetValue(key = new VertexKey(vertices[i3]), out entry)) {
	                    entry = new List<VertexEntry>();
	                    dictionary.Add(key, entry);
	                }
	                entry.Add(new VertexEntry(subMeshIndex, triIndex, i3));

                    triangleCenter = Vector3.zero;
                    triangleCenter += vertices[i1];
                    triangleCenter += vertices[i2];
                    triangleCenter += vertices[i3];
                    triangleCenter *= 0.33333333333f;

                    float distToCenter = (triangleCenter - vertices[i1]).magnitude * objScale;

                    triangleCenterUV1[i1].x = triangleCenter.x;
                    triangleCenterUV1[i1].y = triangleCenter.y;
                    triangleCenterUV2[i1].x = triangleCenter.z;
                    triangleCenterUV2[i1].y = distToCenter;

                    triangleCenterUV1[i2].x = triangleCenter.x;
                    triangleCenterUV1[i2].y = triangleCenter.y;
                    triangleCenterUV2[i2].x = triangleCenter.z;
                    triangleCenterUV2[i2].y = distToCenter;

                    triangleCenterUV1[i3].x = triangleCenter.x;
                    triangleCenterUV1[i3].y = triangleCenter.y;
                    triangleCenterUV2[i3].x = triangleCenter.z;
                    triangleCenterUV2[i3].y = distToCenter;
	            }
	        }

	        // Each entry in the dictionary represents a unique vertex position.
	        foreach (var vertList in dictionary.Values) {
                weightDict.Clear();

	            for (var i = 0; i < vertList.Count; ++i) {
	                var sum = new Vector3();
	                var lhsEntry = vertList[i];

                    // average weighs
                    if(boneWeights != null){
                        int vid = lhsEntry.VertexIndex;

                        for(int boneIt = 0; boneIt < maxBonesPerVert; ++boneIt){
                            int boneId = boneWeights[(vid * maxBonesPerVert) + boneIt].boneIndex;
                            float weight = boneWeights[(vid * maxBonesPerVert) + boneIt].weight;

                            if(!weightDict.ContainsKey(boneId)){
                                weightDict[boneId] = 0.0f;
                            }

                            weightDict[boneId] += weight;
                        }
                    }

                    // compute normals
	                for (var j = 0; j < vertList.Count; ++j) {
	                    var rhsEntry = vertList[j];

	                    if (lhsEntry.VertexIndex == rhsEntry.VertexIndex) {
	                        sum += triNormals[rhsEntry.MeshIndex][rhsEntry.TriangleIndex];
	                    } 
                        else {
	                        // The dot product is the cosine of the angle between the two triangles.
	                        // A larger cosine means a smaller angle.
	                        var dot = Vector3.Dot(
	                            triNormals[lhsEntry.MeshIndex][lhsEntry.TriangleIndex],
	                            triNormals[rhsEntry.MeshIndex][rhsEntry.TriangleIndex]);
	                        if (dot >= cosineThreshold) {
	                            sum += triNormals[rhsEntry.MeshIndex][rhsEntry.TriangleIndex];
	                        }
	                    }
	                }

	                normals[lhsEntry.VertexIndex] = sum.normalized;
	            }

                if(boneWeights != null){
                    for(var vit = 0; vit < vertList.Count; ++vit) {
                        int vid = vertList[vit].VertexIndex;

                        int boneIt0 = 0;
                        foreach(KeyValuePair<int, float> entry in weightDict){
                            int boneId = entry.Key;
                            float weightValue = entry.Value / vertList.Count;

                            boneWeights[(vid * maxBonesPerVert) + boneIt0].boneIndex = boneId;
                            boneWeights[(vid * maxBonesPerVert) + boneIt0].weight = weightValue;

                            boneIt0 += 1;
                        }

                        // descending order sort
                        for(int boneIt = 0; boneIt < maxBonesPerVert; ++boneIt){
                            float value = boneWeights[(vid * maxBonesPerVert) + boneIt].weight;
                            int boneId = boneWeights[(vid * maxBonesPerVert) + boneIt].boneIndex;

                            int j = 0;
                            for(j = boneIt - 1; j >= 0 && boneWeights[(vid * maxBonesPerVert) + j].weight < value; j--){
                                boneWeights[(vid * maxBonesPerVert) + j + 1] = boneWeights[(vid * maxBonesPerVert) + j];
                            }

                            boneWeights[(vid * maxBonesPerVert) + j + 1].boneIndex = boneId;
                            boneWeights[(vid * maxBonesPerVert) + j + 1].weight = value;
                        }
                    }
                }
	        }

	        mesh.normals = normals;
            mesh.uv3 = triangleCenterUV1;
            mesh.uv4 = triangleCenterUV2;
            
            if(boneWeights != null){
                NativeArray<byte> numBonesPerVertArr = new NativeArray<byte>(numBonesPerVert, Unity.Collections.Allocator.Persistent);
                NativeArray<BoneWeight1> boneWeightsArr = new NativeArray<BoneWeight1>(boneWeights, Unity.Collections.Allocator.Persistent);

                mesh.SetBoneWeights(numBonesPerVertArr, boneWeightsArr);

                numBonesPerVertArr.Dispose();
                boneWeightsArr.Dispose();
            }
	    }
         
