    	public static void weldVertices(Mesh aMesh) {
    		var verts = aMesh.vertices;
    		var cols = aMesh.colors;
    		Dictionary<Vector3, int> duplicateHashTable = new Dictionary<Vector3, int>();
    		int[] newVerts = aMesh.triangles;
    		int[] map = new int[verts.Length];

    		int newVertsSize = 0;
    		for (int i = 0; i < verts.Length; i++) {
    			Vector3 v = verts[i];
    			v.x = (float)System.Math.Round((double)v.x, 3);
    			v.y = (float)System.Math.Round((double)v.y, 3);
    			v.z = (float)System.Math.Round((double)v.z, 3);
    			
    			if (!duplicateHashTable.ContainsKey(v)) {
    				duplicateHashTable[v] = newVertsSize;
    				map[i] = newVertsSize;
    				newVerts[newVertsSize] = i;
    				newVertsSize++;
    			}
    			else {
    				map[i] = duplicateHashTable[v];
    			}
    		}

    		var verts2 = new Vector3[newVertsSize];
    		var cols2 = new Color[newVertsSize];
    		for (int i = 0; i < newVertsSize; i++) {
    			int a = newVerts[i];
    			verts2[i] = verts[a];
    			cols2[i] = cols[a];
    		}
    		
    		var tris = aMesh.triangles;
    		for (int i = 0; i < tris.Length; i++) {
    			tris[i] = map[tris[i]];
    		}

    		aMesh.Clear();
    		aMesh.vertices = verts2;
    		aMesh.triangles = tris;
    		aMesh.colors = cols2;
    	}

